package gift.DTO;

public class WishDTO {

    private Long id;
    private String comment;

    public WishDTO() {}

    public WishDTO(Long id, String comment) {
        this.id = id;
        this.comment = comment;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getComment() {
        return comment;
    }

    public void setComment(String comment) {
        this.comment = comment;
    }
}
